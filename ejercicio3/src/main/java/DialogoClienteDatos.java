public class DialogoClienteDatos {
    private String nombre;
    private String dni;
    private boolean pulsadoOk;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public boolean isPulsadoOk() {
        return pulsadoOk;
    }

    public void setPulsadoOk(boolean pulsadoOk) {
        this.pulsadoOk = pulsadoOk;
    }
}
